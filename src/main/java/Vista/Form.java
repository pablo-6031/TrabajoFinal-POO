    private void tablaEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEmpleadosMouseClicked
        // TODO add your handling code here:
        //obtenemos la posicion x y y 
        int column = tablaEmpleados.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY()/tablaEmpleados.getRowHeight();
        
        if (row < tablaEmpleados.getRowCount() && row >= 0 && column < tablaEmpleados.getColumnCount() && column >= 0){
            Object value = tablaEmpleados.getValueAt(row, column);
            if(value instanceof JButton){
               ((JButton)value).doClick();
               JButton boton = (JButton) value;
               
               if(boton.getName().equals("edit")){
                   int fila = tablaEmpleados.getSelectedRow();
                   txtLegajo.setText(this.tablaEmpleados.getValueAt(fila, 0).toString());
                   txtNombreEmpleado.setText(this.tablaEmpleados.getValueAt(fila, 1).toString());
                   txtApellidoEmpleado.setText(this.tablaEmpleados.getValueAt(fila, 2).toString());
                   txtCuit.setText(this.tablaEmpleados.getValueAt(fila, 3).toString());
                   txtFecha.setDate(new Date(this.tablaEmpleados.getValueAt(fila, 4).toString()));
                   txtSueldo.setText(this.tablaEmpleados.getValueAt(fila, 5).toString());
                   
               }
               if(boton.getName().equals("del")){
                   int fila = tablaEmpleados.getSelectedRow();
                   int id = Integer.parseInt(this.tablaEmpleados.getValueAt(fila, 0).toString());
                   eliminar(id);
                   JOptionPane.showMessageDialog(null, "El empleado fue eliminado");
                
               }
            }
        }
    }//GEN-LAST:event_tablaEmpleadosMouseClicked

